package Anarchy.Module.Commands.Home.Utils;

public class HomeUtils {
	private Integer X;
	private Integer Y;
	private Integer Z;

	public HomeUtils(Integer X, Integer Y, Integer Z) {
		this.X = X;
		this.Y = Y;
		this.Z = Z;
	}

	public Integer getX() {
		return X;
	}

	public Integer getY() {
		return Y;
	}

	public Integer getZ() {
		return Z;
	}
}